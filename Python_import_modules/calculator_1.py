def add(a, b):
    """This function add two numbers"""
    return a + b

def subtract(a, b):
    """This function subtract two numbers"""
    return a - b

def multiply(a, b):
    """This function multiply two numbers"""
    return a * b

def quotient(a, b):
    """This function finds the remender of a number"""
    return a % b

def devide(a, b):
    """This function devide  two numbers"""
    return a / b
